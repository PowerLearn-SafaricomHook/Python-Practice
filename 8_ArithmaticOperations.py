
# Arithmatic Operations in python

# Python supports the following arithmetic operators:

# + (Addition)
# - (Subtraction)
# * (Multiplication)
# / (Division)
# % (Modulus)
# ** (Exponent)
# // (Floor Division)

# Addition
# The addition operator + adds two numbers.

# example:
# add two numbers
num1 = 5
num2 = 10
sum = num1 + num2
print(sum)  # 15

# Subtraction
# The subtraction operator - subtracts one number from another.

# example:
# subtract two numbers
num1 = 5
num2 = 10
sub = num2 - num1
print(sub)  # 5

# Multiplication
# The multiplication operator * multiplies two numbers.

# example:
# multiply two numbers
num1 = 5
num2 = 10
mul = num1 * num2
print(mul)

# Division
# The division operator / divides one number by another.

# example:

# divide two numbers
num1 = 5
num2 = 10
div = num2 / num1
print(div)  # 2.0

