
# Arithmatic Operations in python

# Python supports the following arithmetic operators:

# + (Addition)
# - (Subtraction)
# * (Multiplication)
# / (Division)
# % (Modulus)
# ** (Exponent)
# // (Floor Division)

# Addition
# The addition operator + adds two numbers.

# example:
# add two numbers
num1 = 5
num2 = 10
sum = num1 + num2
print(sum)  # 15

# Subtraction
# The subtraction operator - subtracts one number from another.

# example:
# subtract two numbers
num1 = 5
num2 = 10
sub = num2 - num1
print(sub)  # 5

# Multiplication
# The multiplication operator * multiplies two numbers.

# example:
# multiply two numbers
num1 = 5
num2 = 10
mul = num1 * num2
print(mul)

# Division
# The division operator / divides one number by another.

# example:

# divide two numbers
num1 = 5
num2 = 10
div = num2 / num1
print(div)  # 2.0

# Modulus
# The modulus operator % returns the remainder of the division of two numbers.

# example:
# find the remainder
num1 = 5
num2 = 10
mod = num2 % num1
print(mod)  # 0

# Exponent
# The exponent operator ** raises the first number to the power of the second number.

# example:
# find the exponent
num1 = 5
num2 = 10
exp = num1 ** num2
print(exp)  # 9765625

# Floor Division
# The floor division operator // divides one number by another and returns the integer part of the result.

# example:
# divide two numbers and return the integer part
num1 = 5
num2 = 10
floor_div = num2 // num1
print(floor_div)  # 2




