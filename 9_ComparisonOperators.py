
# Comparison Operators in Python

# Equal to

# The equal to operator == compares two values and returns True if they are equal, and False if they are not.
a = 5
b = 5
print(a == b)  # True

# Not equal to
a = 5
b = 3
print(a != b)  # True

# Greater than
a = 7
b = 5
print(a > b)  # True

# Less than
a = 3
b = 5
print(a < b)  # True

# Greater than or equal to
a = 5
b = 5
print(a >= b)  # True

# Less than or equal to
a = 3
b = 5
print(a <= b)  # True